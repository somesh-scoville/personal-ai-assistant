"""Inspect the tool calls for Trustcall."""

from trustcall import create_extractor

from agents.tools import Profile, ToDo
from core.llm import model


class Spy:
    def __init__(self):
        self.called_tools = []

    def __call__(self, run):
        q = [run]
        while q:
            r = q.pop()
            if r.child_runs:
                q.extend(r.child_runs)
            if r.run_type == "chat_model":
                self.called_tools.append(
                    r.outputs["generations"][0][0]["message"]["kwargs"]["tool_calls"]
                )


# Extract information from tool calls for both patches and new memories in Trustcall
def extract_tool_info(tool_calls, schema_name: str = "Memory") -> str:
    """Extract information from tool calls for both patches and new memories.

    Args:
        tool_calls: List of tool calls from the model
        schema_name: Name of the schema tool (e.g., "Memory", "ToDo", "Profile")
    Returns:
        A formatted string summarizing the changes made.
    """
    # Initialize list of changes
    changes = []

    for call_group in tool_calls:
        for call in call_group:
            if call["name"] == "PatchDoc":
                # Check if there are any patches
                if call["args"]["patches"]:
                    changes.append(
                        {
                            "type": "update",
                            "doc_id": call["args"]["json_doc_id"],
                            "planned_edits": call["args"]["planned_edits"],
                            "value": call["args"]["patches"][0]["value"],
                        }
                    )
                else:
                    # Handle case where no changes were needed
                    changes.append(
                        {
                            "type": "no_update",
                            "doc_id": call["args"]["json_doc_id"],
                            "planned_edits": call["args"]["planned_edits"],
                        }
                    )
            elif call["name"] == schema_name:
                changes.append({"type": "new", "value": call["args"]})

    # Format results as a single string
    result_parts = []
    for change in changes:
        if change["type"] == "update":
            result_parts.append(
                f"Document {change['doc_id']} updated:\n"
                f"Plan: {change['planned_edits']}\n"
                f"Added content: {change['value']}"
            )
        elif change["type"] == "no_update":
            result_parts.append(
                f"Document {change['doc_id']} unchanged:\n{change['planned_edits']}"
            )
        else:
            result_parts.append(f"New {schema_name} created:\nContent: {change['value']}")

    return "\n\n".join(result_parts)


# Initialize the spy for visibility into the tool calls made by Trustcall
spy = Spy()

# Create the Trustcall extractor for updating the ToDo list
todo_extractor = create_extractor(
    model, tools=[ToDo], tool_choice="ToDo", enable_inserts=True
).with_listeners(on_end=spy)


# Trustcall extractor for updating the user's profile
profile_extractor = create_extractor(model, tools=[Profile], tool_choice="Profile")
